<?php

namespace jamesvweston\Stripe\Responses;


class Period
{

    /**
     * @var int
     */
    protected $end;

    /**
     * @var int
     */
    protected $start;

    /**
     * @return int
     */
    public function getEnd(): int
    {
        return $this->end;
    }

    /**
     * @param int $end
     */
    public function setEnd(int $end): void
    {
        $this->end = $end;
    }

    /**
     * @return int
     */
    public function getStart(): int
    {
        return $this->start;
    }

    /**
     * @param int $start
     */
    public function setStart(int $start): void
    {
        $this->start = $start;
    }

}